class Solution {
public:
    vector<vector<int>> levelOrder(Node *root) {
        vector<vector<int>> answer;

        if (root == NULL)
            return answer;

        queue<Node *> q;
        q.push(root);
        while (!q.empty()) {
            int lenLevel = q.size();
            vector<int> NodesInLevel;
            for (int i = 0; i < lenLevel; i++) {
                Node *currentNode = q.front();
                q.pop();
                NodesInLevel.push_back(currentNode->val);
                vector<Node *> children = currentNode->children;
                for (auto child: children)
                    q.push(child);
            }
            answer.push_back(NodesInLevel);
        }
        return answer;
    }
};

